#Escape Sequence

#It adds a slash '\', meaning it tells python that everything after that is a comment
# \t = adds a tab or indent
# \n = makes a new line

weather = "It\'s \"kind of\" sunny" #Make sure to add the \ infront of all quotation marks

print(weather)

weather_1 = "\t It\'s \"kind of\" sunny \n hope you have a good day!" #\t = tab or indent, \n = new line

print(weather_1) 

